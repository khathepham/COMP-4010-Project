# Define two sets
set_A = {1, 2, 3, 4, 5}
set_B = {3, 9, 5, 6, 7}

# Find the complement using the difference operation
complement_result = set_B - set_A
print("Complement using difference operation:", complement_result)

# Find the complement using the - operator
complement_result_operator = set_B - set_A
print("Complement using - operator:", complement_result_operator)
